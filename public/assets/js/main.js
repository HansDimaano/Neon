// $.noConflict(); - Commented out, but normally used to release the global reference to the jQuery variable $, allowing it to be used by other libraries.

jQuery(document).ready(function($) {
	// The code inside this function will be executed when the DOM is ready.

	"use strict"; // Enables strict mode, which enforces stricter rules and prevents certain unsafe actions.

	// SelectFx initialization for select elements with the class 'cs-select'
	[].slice.call( document.querySelectorAll( 'select.cs-select' ) ).forEach( function(el) {
		new SelectFx(el);
	});

	jQuery('.selectpicker').selectpicker;

	// Adding event handlers for the search trigger
	$('.search-trigger').on('click', function(event) {
		event.preventDefault();
		event.stopPropagation();
		$('.search-trigger').parent('.header-left').addClass('open');
	});

	// Adding event handlers for the search close
	$('.search-close').on('click', function(event) {
		event.preventDefault();
		event.stopPropagation();
		$('.search-trigger').parent('.header-left').removeClass('open');
	});

	// Matching the height of elements with the class 'equal-height' based on their maximum height
	$('.equal-height').matchHeight({
		property: 'max-height'
	});

	// Counter Number - Animates the counter numbers
	$('.count').each(function () {
		$(this).prop('Counter',0).animate({
			Counter: $(this).text()
		}, {
			duration: 3000,
			easing: 'swing',
			step: function (now) {
				$(this).text(Math.ceil(now));
			}
		});
	});

	// Menu Trigger
	$('#menuToggle').on('click', function(event) {
		var windowWidth = $(window).width();
		if (windowWidth<1010) {
			$('body').removeClass('open');
			if (windowWidth<760){
				$('#left-panel').slideToggle();
			} else {
				$('#left-panel').toggleClass('open-menu');
			}
		} else {
			$('body').toggleClass('open');
			$('#left-panel').removeClass('open-menu');
		}
	});

	// Adding event handlers for dropdown menu items with children
	$(".menu-item-has-children.dropdown").each(function() {
		$(this).on('click', function() {
			var $temp_text = $(this).children('.dropdown-toggle').html();
			$(this).children('.sub-menu').prepend('<li class="subtitle">' + $temp_text + '</li>');
		});
	});

	// Load Resize - Handling the window load and resize events
	$(window).on("load resize", function(event) {
		var windowWidth = $(window).width();
		if (windowWidth<1010) {
			$('body').addClass('small-device');
		} else {
			$('body').removeClass('small-device');
		}
	});
});
